    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Hospital h = (Hospital)hospital.getSelectedItem();
        JOptionPane.showMessageDialog(rootPane, h.getIdHospital());
        Funcionario f = new Funcionario();
        f.setNome(nome.getText());
        f.setCpf(cpf.getText());
        f.setRg(rg.getText());
        f.setIdade(Integer.parseInt(idade.getText()));
        f.setSenha(senha.getText());
        f.setVlrHora(Integer.parseInt(vlHora.getText()));
        
        
        ControllerFuncionario cf = new ControllerFuncionario();
        try {
            cf.CadastrarFuncionario(f);
        } catch (Exception e) {
            Logger.getLogger(CadastrarFuncionario2.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
    
    
